// // (==) -equal to
let n1=4
let n2=4
console.log(n1==n2)

// (==)-only check equal value
let n3=4
let n4="4"
console.log(n3==n4)

// (===)-check equal value and equal data type
let fl=4
let sl=4
let tl="4"  
console.log(fl===sl)
console.log(fl===tl)

// (!=) - check not equal value
let n5=4
let n6=4
console.log(n5!=n6)
let n7=5
console.log(n5!=n7)
let n8="4"
console.log(n5!=n8)

// (!==)-check not equal value and datatype
let n9=5
let n10=6
let n11="6"
console.log(n9!==n10)
console.log(n9!==n11)


