function result(num1,num2,op){
if(op== "+"){
    console.log(num1+num2)
}else if(op== "-"){
    console.log(num1-num2)
}else if(op== "*"){
    console.log(num1*num2)
}else if(op== "/"){
    console.log(num1/num2)
}else if(op=="%"){
    console.log(num1%num2)
}
else{
    console.log("No Result")
}
}
result(10,100,"/")
result(5,3,"*")